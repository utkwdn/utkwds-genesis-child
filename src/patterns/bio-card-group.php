<h3>Joy Harjo</h3>
<!-- /wp:heading -->

<!-- wp:paragraph -->
<p><em>Honorary Doctor of Humanities, Spring 2022</em>
</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>Joy Harjo, the 23rd poet laureate of the United States, is an internationally renowned performer and writer of the Muscogee (Creek) Nation. Harjo is the author of nine books of poetry, several plays, children’s books, and two memoirs; she has also edited several anthologies and produced seven award-winning music albums. Her many honors include the Ruth Lily Prize from the Poetry Foundation, the Academy of American Poets Wallace Stevens Award, two NEA fellowships, and a Guggenheim Fellowship. </p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>A former John C. Hodges Chair of Excellence at UT, Harjo is currently a chancellor of the Academy of American Poets and chair of the Native Arts and Cultures Foundation Board of Directors. She is a member of the National Women’s Hall of Fame, the National Native American Hall of Fame, the American Philosophical Society, and the American Academy of Arts and Letters. She lives in Tulsa, Oklahoma, where she is the inaugural artist-in-residence of the Bob Dylan Center. </p>
