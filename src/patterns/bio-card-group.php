<h3>Bill Dance</h3>
<!-- /wp:heading -->

<!-- wp:paragraph -->
<p><em>Honorary Doctor of Natural Resources, Spring 2021</em>
</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>As a professional fisher, television personality, author, conservationist, teacher, and native Tennessean, Bill Dance is one of the most recognizable outdoor sports figures in the world. Dance—whose trademark for 40 years has been an orange-and-white UT baseball cap—quickly gained notoriety and success during his time on the professional bass fishing tournament trail in the 1970s. His notable accomplishments include three-time B.A.S.S. Angler of the Year, eight-time Bassmaster Classic Qualifier, seven tournament wins, eight runners up, six third place, and 40 top 10 finishes. However, most people know him through his television show <em>Bill Dance Outdoors</em>, which is one of the longest-running television shows in history with well over 2,000 episodes. Dance is also the author of seven books and has been a regular contributor to national magazines. </p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>His contributions to outdoor recreation, conservation, and economic development have been recognized across the globe. His notable awards include the Congressional National Water Safety Award; induction into the National Freshwater Hall of Fame; induction into the Professional Bass Fishing Hall of Fame; the President’s Award for the National Association of State Boating Law Administrators; induction into the International Game Fish Association Hall of Fame; and being honored by the Tennessee Sports Hall of Fame as both as an inductee and winner of the Tennessee Male Professional Athlete of the Year award. </p>
