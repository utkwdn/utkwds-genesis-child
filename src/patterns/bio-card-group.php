<h3>Scott Niswonger</h3>
<!-- /wp:heading -->

<!-- wp:paragraph -->
<p><em>Honorary Doctor of Humane Letters, Spring 2018</em></p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>Scott Niswonger&nbsp;was born in Van Wert, Ohio, where he learned to fly, soloing on his 16th birthday. He has flown everything from J-3 Cubs to the Boeing 747, and he enjoys flying his Gulfstream IV and his aerobatic Navy T-34 trainer. After graduation from Purdue, Niswonger moved to Greeneville, Tennessee, as the corporate pilot for the president of the Magnavox Company. Niswonger co-founded Landair Services and later formed Forward Air Corporation. He is executive chairman of Landair Transport Inc. and chairman emeritus of Forward Air Corporation. He is a member of the board of directors of First Horizon National Corporation, the parent organization of First Tennessee Bank. He is chairman of Tennessee State University board of trustees and a trustee of Tusculum College. He is especially proud of the Niswonger Educational Foundation, which he established in 2001 to create opportunities for individual and regional growth through educational programs, scholarships, and other charitable activities. Niswonger is the lead benefactor for Niswonger Children’s Hospital, home to one of only eight St. Jude affiliate clinics in the world. </p>
