<h3>Theotis Robinson Jr.</h3>
<!-- /wp:heading -->

<!-- wp:paragraph -->
<p><em>Honorary Doctor of Social Work, Fall 2019</em></p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>After opening UT’s doors for African American undergraduates, Theotis Robinson Jr. has spent his lifetime continuing to advance social justice.
</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>It was Robinson’s persistence in pursuing his application that eventually led to a change in the university’s admissions policy in November 1960. He became the first African American undergraduate student admitted and was one of three who enrolled in January 1961. </p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>Robinson went on to serve on Knoxville’s City Council—where he was the first African American representative elected in more than 50 years—and as vice president of economic development for the 1982 World’s Fair. He taught political science and served in purchasing and government relations at UT before being vice president of equity and diversity for the UT System in 2000, a role he held until his retirement in 2014. Robinson still gives occasional lectures and writes political opinion columns. </p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>Among numerous other honors, Robinson is a charter member of UT’s African American Hall of Fame and was named by <em>Metro Pulse</em> newspaper as one of the 100 most influential Knoxvillians of the 20th century. </p>
